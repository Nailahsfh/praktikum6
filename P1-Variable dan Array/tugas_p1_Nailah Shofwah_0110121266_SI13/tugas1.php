2. Apa persamaan variabel system dengan variabel konstan? 
<br>
Variabel system adalah suatu tempat untuk menampung data yang nilainya selalu berubah. Sedangkan, Konstanta adalah suatu tempat untuk menampung data yang nilainya selalu tetap dan tidak pernah berubah. Jadi persamaan variabel system dan variabel konstan adalah sama. suna untuk menampung data.