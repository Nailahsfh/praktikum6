-Fungsi array_sort() digunakan untuk menyortir elemen-elemen dari array yang diindeks dalam ascending order (abjad untuk huruf dan angka untuk angka).
<br>
-Fungsi array_pop() berguna untuk menghapus elemen terakhir dari sebuah array.
<br>
-Fungsi array_push digunakan untuk menambahkan elemen baru ke akhir array.
<br>
-Fungsi array_shift() menghapus elemen pertama dari array, dan mengembalikan nilai elemen yang dihapus.
<br>
-Fungsi array_unshift() berguna untuk menyisipkan elemen baru ke array. Nilai array baru akan disisipkan di awal array.
